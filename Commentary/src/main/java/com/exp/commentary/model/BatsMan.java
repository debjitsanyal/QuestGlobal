package com.exp.commentary.model;

public class BatsMan {
	
	private String name;
	private Integer currentRun;
	private Integer battingOrder;
	private Boolean onStrick;
	private Integer numberOfSix;
	private Integer numberOfFoure;
	private Integer numberOfSingle;
	private Integer numberOfDot;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getCurrentRun() {
		return currentRun;
	}
	public void setCurrentRun(Integer currentRun) {
		this.currentRun = currentRun;
	}
	public Integer getBattingOrder() {
		return battingOrder;
	}
	public void setBattingOrder(Integer battingOrder) {
		this.battingOrder = battingOrder;
	}
	public Boolean getOnStrick() {
		return onStrick;
	}
	public void setOnStrick(Boolean onStrick) {
		this.onStrick = onStrick;
	}
	public Integer getNumberOfSix() {
		return numberOfSix;
	}
	public void setNumberOfSix(Integer numberOfSix) {
		this.numberOfSix = numberOfSix;
	}
	public Integer getNumberOfFoure() {
		return numberOfFoure;
	}
	public void setNumberOfFoure(Integer numberOfFoure) {
		this.numberOfFoure = numberOfFoure;
	}
	public Integer getNumberOfSingle() {
		return numberOfSingle;
	}
	public void setNumberOfSingle(Integer numberOfSingle) {
		this.numberOfSingle = numberOfSingle;
	}
	public Integer getNumberOfDot() {
		return numberOfDot;
	}
	public void setNumberOfDot(Integer numberOfDot) {
		this.numberOfDot = numberOfDot;
	}
	
	

}
